# Escape sequences are used to represent certain special characters within strings in Python.
# Here are some common escape sequences:

# \n - Newline: Moves the cursor to the next line.
print("Hello, World!\nWelcome to Python programming.")

# \t - Tab: Adds a horizontal tab space.
print("Item\t\tPrice\nApple\t\t$1\nBanana\t\t$0.5")

# \\ - Backslash: Prints a single backslash.
print("This is a backslash: \\")

# \' - Single Quote: Prints a single quote.
print('It\'s a sunny day.')

# \" - Double Quote: Prints a double quote.
print("He said, \"Hello!\"")

# \r - Carriage Return: Moves the cursor to the beginning of the line.
print("Hello, World!\rWelcome")  # "Welcomeo, World!"

# \b - Backspace: Deletes the previous character.
print("Hello, World!\b!")  # "Hello, World!"

# \f - Form Feed: Moves the cursor to the next page (not commonly used in modern systems).
print("Hello, World!\fWelcome")

# \v - Vertical Tab: Adds a vertical tab space.
print("Hello,\vWorld!")

# \a - Bell/Alert: Triggers a system alert sound (if the system supports it).
print("Hello, World!\a")

