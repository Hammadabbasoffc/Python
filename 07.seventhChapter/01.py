#intro to dictionary with python

# A dictionary is a collection of key-value pairs. Each key-value pair is separated by a colon (:), and the pairs are separated by commas (,).

# Dictionaries are mutable, which means you can add, remove, or update key-value pairs after they are created.

# Dictionaries are defined with curly braces ({}) and can contain different data types for keys and values.

# Dictionaries have built-in methods to retrieve, add, remove, and update key-value pairs, such as get(), update(), pop(), and clear().

my_dict = {
    'name': 'John',
    'age': 30,
    'city': 'New York'
}

# Accessing values using keys

print(my_dict['name'])  # Output: John
print(my_dict['age'])  # Output: 30